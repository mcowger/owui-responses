def truncate_for_log(value: Any, limit: int = 2000) -> tuple[str, bool]:
    """Return a safe, possibly truncated string for logging."""

    if value is None:
        return "", False
    text = value if isinstance(value, str) else str(value)
    if len(text) <= limit:
        return text, False
    return text[:limit], True


