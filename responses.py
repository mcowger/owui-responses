def _coerce_tool(raw: dict) -> dict | None:
    """Validate and normalize a raw tool dict against the known models."""

    tool_type = raw.get("type")
    if not isinstance(tool_type, str):
        return None
    cleaned = deepcopy(raw)
    cleaned.pop("source", None)
    model = _TOOL_MODEL_MAP.get(tool_type)
    if model is None:
        return cleaned
    try:
        return model.model_validate(cleaned).model_dump(exclude_none=True)
    except ValidationError as exc:
        name = cleaned.get("name") if isinstance(cleaned.get("name"), str) else None
        descriptor = f"{tool_type}:{name}" if name else str(tool_type)
        _logger.warning("Dropping tool %s after validation error: %s", descriptor, exc)
        return None


def _tool_identity(tool: dict) -> tuple[str | None, str | None]:
    tool_type = tool.get("type")
    if tool_type == "function":
        name = tool.get("name")
    elif tool_type == "mcp":
        name = tool.get("server_label") or tool.get("server_url")
    elif tool_type == "web_search":
        name = _stable_json_key(tool)
    else:
        name = tool.get("id") or tool.get("name")
    return (str(tool_type) if tool_type is not None else None, name)


