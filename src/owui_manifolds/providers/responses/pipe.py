def _server_tool_event_phase(event_type: str | None) -> tuple[str, str] | None:
    """Return (tool_type, phase) for Responses API server-side tool events."""

    if not isinstance(event_type, str) or not event_type.startswith("response."):
        return None
    body = event_type[len("response.") :]
    tool_type, sep, phase = body.rpartition(".")
    if not sep or tool_type not in _SERVER_SIDE_TOOL_TYPES:
        return None
    return tool_type, phase


def _server_tool_item_detail(item: dict[str, Any] | None) -> str:
    if not isinstance(item, dict):
        return ""

    action = item.get("action") if isinstance(item.get("action"), dict) else {}
    query = action.get("query") or item.get("query")
    if isinstance(query, str) and query.strip():
        return f" for `{query.strip()}`"

    queries = item.get("queries") or action.get("queries")
    if isinstance(queries, list):
        clean_queries = [str(q).strip() for q in queries if str(q).strip()]
        if clean_queries:
            joined = ", ".join(f"`{q}`" for q in clean_queries[:3])
            suffix = "…" if len(clean_queries) > 3 else ""
            return f" for {joined}{suffix}"

    name = item.get("name") or item.get("server_label") or action.get("name")
    if isinstance(name, str) and name.strip():
        return f" `{name.strip()}`"

    return ""


def _server_tool_sources_count(item: dict[str, Any] | None) -> int:
    if not isinstance(item, dict):
        return 0
    action = item.get("action") if isinstance(item.get("action"), dict) else {}
    sources = action.get("sources") or item.get("sources")
    return len(sources) if isinstance(sources, list) else 0


